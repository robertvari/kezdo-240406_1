number = 13

#    False
if number == 10:
    print("The number is 10")

#    True
if number != 10:
    print("Number is not 10")

#    False
if number < 10:
    print("Number is less than 10")

#   True
if number > 10:
    print("Number is greater than 10")

#   False
if number <= 10:
    print("Number is less or equal to 10")

#   True
if number >= 10:
    print("Number is greater or equal to 10")